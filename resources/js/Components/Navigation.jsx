/**
 * Check if the current route is a package route
 * @param route
 * @returns {*}
 */
const isPackageRoute = (route) => {
    return (
        route().current("package.index") ||
        route().current("package.create") ||
        route().current("package.services") ||
        route().current("package.edit")
    )
}

